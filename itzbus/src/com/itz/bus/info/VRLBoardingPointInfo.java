package com.itz.bus.info;

public class VRLBoardingPointInfo {
	
	private int boarding_point_id;
	
	private String boarding_point_name;
	
	private String boarding_point_time;

	/**
	 * @return the boarding_point_id
	 */
	public int getBoarding_point_id() {
		return boarding_point_id;
	}

	/**
	 * @param boardingPointId the boarding_point_id to set
	 */
	public void setBoarding_point_id(int boardingPointId) {
		boarding_point_id = boardingPointId;
	}

	/**
	 * @return the boarding_point_name
	 */
	public String getBoarding_point_name() {
		return boarding_point_name;
	}

	/**
	 * @param boardingPointName the boarding_point_name to set
	 */
	public void setBoarding_point_name(String boardingPointName) {
		boarding_point_name = boardingPointName;
	}

	/**
	 * @return the boarding_point_time
	 */
	public String getBoarding_point_time() {
		return boarding_point_time;
	}

	/**
	 * @param boardingPointTime the boarding_point_time to set
	 */
	public void setBoarding_point_time(String boardingPointTime) {
		boarding_point_time = boardingPointTime;
	}
	
	

}
